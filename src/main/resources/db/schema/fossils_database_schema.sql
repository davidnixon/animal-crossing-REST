-- Drop tables if they exist
DROP TABLE IF EXISTS FOSSIL_NAMES;
DROP TABLE IF EXISTS FOSSILS;

CREATE TABLE FOSSILS
(
    ID            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FILE_NAME     VARCHAR(64) NOT NULL UNIQUE,
    PRICE         INTEGER,
    MUSEUM_PHRASE VARCHAR(1024),
    IMAGE_URI     VARCHAR(128),
    PART_OF       VARCHAR(32)
);

-- Localized names
CREATE TABLE FOSSIL_NAMES
(
    ID        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FOSSIL_ID INTEGER      NOT NULL,
    LOCALE    VARCHAR(32)  NOT NULL,
    NAME      VARCHAR(128) NOT NULL,
    FOREIGN KEY (FOSSIL_ID) REFERENCES FOSSILS (ID) ON DELETE CASCADE
);
